# ⁡⁢⁣⁣​‌‍‌𝗔𝗹𝗹 𝗧𝘆𝗽𝗲𝘀 𝗼𝗳 𝗢𝗽𝗲𝗿𝗮𝘁𝗼𝗿𝘀​⁡
# Arithmetic Operators
a = 10
b = 5
print(a + b)  # 15
print(a - b)  # 5
print(a * b)  # 50
print(a / b)  # 2.0
print(a // b)  # 2
print(a % b)  # 0
print(a ** b)  # 100000

# Comparison Operators
print(a ==b)  # False
print(a != b)  # True
print(a > b)  # True
print(a < b)  # False
print(a >= b)  # True
print(a <= b)  # False

# Assignment Operators
a = 10

a += 5
print(a)  # 15
a -= 5
print(a)  # 10
a *= 5
print(a)  # 50
a /= 5
print(a)  # 10.0
a //= 5
print(a)  # 2.0
a %= 5
print(a)  # 2.0
a **= 5
print(a)  # 32.0

# Logical Operators
a = 10
b = 5


print(a > 5 and b < 10)  # True
print(a > 5 or b > 10)  # True
print(not(a > 5))  # False

# Bitwise Operators
a = 10  # 1010 in binary
b = 4   # 0100 in binary
print(a & b)  # 0 (0000 in binary)
print(a | b)  # 14 (1110 in binary)
print(a ^ b)  # 14 (1110 in binary)
print(~a)  # -11 (1011 in binary)
print(a << 2)  # 40 (101000 in binary)
print(a >> 2)  # 2 (0010 in binary

# Membership Operators
print(3 in [1, 2, 3])  # True
print(4 not in [1, 2, 3])  # True

# Identity Operators
a = 10
b = 10
print(a is b)  # True
print(a is not b)  # False